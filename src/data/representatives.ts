// Helper functions
export function getRepBySlug(slug: string): Representative | undefined {
  return representatives.find((r) => r.slug === slug);
}

export function getLeadership(): Representative[] {
  return representatives.filter((r) => r.isLeadership);
}

export function getFeatured(): Representative[] {
  return representatives.filter((r) => r.featured && !r.isLeadership);
}

export function searchReps(query: string): Representative[] {
  const q = query.toLowerCase();
  return representatives.filter(
    (r) =>
      r.fullName.toLowerCase().includes(q) ||
      r.state.toLowerCase().includes(q) ||
      r.stateAbbr.toLowerCase() === q
  );
}

export function filterReps(filters: {
  state?: string;
  chamber?: string;
  party?: string;
}): Representative[] {
  return representatives.filter((r) => {
    if (filters.state && r.stateAbbr !== filters.state) return false;
    if (filters.chamber && r.chamber !== filters.chamber) return false;
    if (filters.party && r.party !== filters.party) return false;
    return true;
  });
}

